    public void ListarTabla(){ 
            try
        {
        String[] cabecera = new String [] {"ID", "Nombre", "Duracion(Minutos)"};
        DefaultTableModel modelo = new DefaultTableModel(cabecera, 0);
        CancionDAO oCancionDAO = new CancionDAO(); 
        ArrayList<Cancion> lstCancion = oCancionDAO.ListarCancion();
        
        Object[] fila = null;
        for(Cancion cancion : lstCancion)
        {
            fila = new Object[] {
                cancion.getId(), 
                cancion.getNombre(),
                cancion.getDuracion(),
                
            };
            modelo.addRow(fila);
        }
        
        tbListaCancion.setModel(modelo);   
        }
        catch(CancionException ex)
        {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(), "Error al Cargar Canciones", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }
