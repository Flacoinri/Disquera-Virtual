    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String idBuscado = txtBuscar.getText().trim();
        
        if (idBuscado.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe ingresar un ID para buscar.", "Advertencia", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        try {
            disquera.datos.CancionDAO dao = new disquera.datos.CancionDAO();
            disquera.beans.Cancion cancion = dao.BuscarCancion(idBuscado);
            
            String[] cabecera = {"ID", "Nombre", "Duración"};
            javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel(cabecera, 0);
            
            Object[] fila = new Object[] {
                cancion.getId(),
                cancion.getNombre(),
                cancion.getDuracion()
            };
            modelo.addRow(fila);
            
            tbListaCancion.setModel(modelo);

        } catch (disquera.exceptions.CancionException ex) {
            javax.swing.JOptionPane.showMessageDialog(this, ex.getMessage(), "Error de Búsqueda", javax.swing.JOptionPane.ERROR_MESSAGE);
            
            javax.swing.table.DefaultTableModel modeloVacio = (javax.swing.table.DefaultTableModel) tbListaCancion.getModel();
            modeloVacio.setRowCount(0);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

