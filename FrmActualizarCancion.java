    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        try
        {
         if (txtId.getText().trim().isEmpty() || 
                txtNombre.getText().trim().isEmpty() || 
                txtDuracion.getText().trim().isEmpty()) {
                
                JOptionPane.showMessageDialog(this, "Debe completar todos los campos.", "Advertencia", JOptionPane.WARNING_MESSAGE);
                return;
            }

            String nombre = txtNombre.getText().trim();
            String id = txtId.getText().trim();
            double duracion = Double.parseDouble(txtDuracion.getText().trim());

            Cancion cancion = new Cancion(nombre, duracion, id);
            
            CancionDAO dao = new CancionDAO();
            boolean exito = dao.ModificarCancion(cancion);

            if (exito) {
                JOptionPane.showMessageDialog(this, "Canción actualizada correctamente.", "Información", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            }

        } catch (NumberFormatException nex) {
            JOptionPane.showMessageDialog(this, "La duración debe ser un número válido.", "Error de Formato", JOptionPane.WARNING_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnActualizarActionPerformed

